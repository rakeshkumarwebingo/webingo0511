			<h4>Server location and performance</h4>
			<p style="text-align:justify;">One of the easier factors to understand — the farther away your server, the longer it will take your website to load. That being said, it’s important to be mindful of where your server is located. For example, if your business is based in the United States, don’t select a server in China.<br>
			In relation to server performance, you want to ensure that you have a high-quality website hosting service. Here’s how it works:</p>
			<p>➣ A user types your URL into their search bar, or visits your website from a search engine like Google.</p>
			<p>➣ Their browser notifies your server that they’re trying to visit your site, and asks for all the information necessary to visit your page.</p>
			<p>➣ If your server is high-quality, the information will arrive quickly.</p>
			<p>The quality of your server comes down to whether it’s shared or dedicated:</p>
			<p>➣ Are you on a shared server? If so, you are sharing server space with another website and their traffic.</p>
			<p>➣ Are you on a dedicated server? If so, you can ensure that you’re not sharing website traffic with another website. When you’re on a dedicated server, you’ll have much faster loading time.</p>
