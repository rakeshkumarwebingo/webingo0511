			<h4>Sloppy HTML coding</h4>
			<p style="text-align:justify;">One surefire way to ensure that your pages will load slowly is by having a sloppy HTML code on the backend of your site.<br>
			HTML code is what tells your page how to look when users view it on your website. The code is made up of all sorts of tags that tell the page how to work and what to do.<br>
			Every page on your website will have a page of code, and the larger and more complex your website is, the more complex your code will be. This is where it becomes easy for your code to become jumbled and sloppy.<br>
			In order to keep your pages loading quickly, it’s important to ensure that there are no extra tags, and that everything is referenced properly. Otherwise, it will take the server longer to read the code — causing a slow-loading site.</p>
		</div>
	</div>
	<div class="media mt40">
		<img src="images/icons/combination.png" alt="icon">
		<div class="media-body">
			<h4>Huge, unoptimized graphics</h4>
			<p style="text-align:justify;">Including graphics on your site is crucial to providing a great user experience. When you provide a whole page of information, for example, it’s important to break up all that information with some images, graphics, or video to ensure that the reader doesn’t get bored.<br>
			However, all the bells and whistles that you can add to your site pages in terms of graphics and videos can weigh your site down and cause it to load slowly.<br>
			Huge, high-resolution photos are one of the biggest causes of slow-loading pages.<br>
			So how do you ensure that your site provides a great user experience with graphics while keeping page load time fast? Optimize your graphics!<br>
			Consider using a tool like Kraken.io to condense your image files so that they’re not so bulky. It won’t change the visual size of the image but condense the file size so that it doesn’t take as long to load on your website.</p>
		</div>
	</div>
	
</div>
