			<h4>Too many redirects</h4>
			<p style="text-align:justify;">
				Redirects are created when you want to replace all links to an old webpage with references to a new one. The redirects are bits of code that are placed within your .htaccess file in the server, and you can create as many as you need when you replace old pages on your site.<br>
				However, when you have tons of redirects on the same page, your site takes the time to reference the .htaccess file to find out the new location of where the link is pointing. This is essentially like loading links twice on your page which ramps up page load time.</p>
		</div>
	</div>
	<div class="media mt40">
