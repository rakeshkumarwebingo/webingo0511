			<p style="text-align:justify;">Signals of intent are the context clues and data that tell us what a potential customer is doing. A signal of intent to buy a pair of shoes, for example, might be someone doing a Google search for shoes in their size. Other factors that can contribute to signals of intent include the activity around the search. If they’ve already been searching for shoes for a while, they may be closer to making a purchase. If they’re searching from a mobile device while in a shoe store, they could be looking for a cheaper option or to see if their size is available online.<br>
